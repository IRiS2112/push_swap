#include "../checker_bonus.h"


int	ft_strcmp(char *s1, char *s2)
{
	size_t	i;

	i = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return(0);
		i++;
	}
	if (s1[i] == '\n' && s2[i] == '\0')
		return (1);
	return (0);
}