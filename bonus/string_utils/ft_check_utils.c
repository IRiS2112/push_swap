#include "../checker_bonus.h"

int	check_m(char *str, int s)
{
	if (str[0] == '-' && ft_strlen(str) >= 11)
	{
		++str;
		while (*str != 0 || "-2147483647"[s] != 0)
		{
			if ((*str - "-2147483647"[s]) > 0)
				return (1);
			s++;
			++str;
		}
	}
	if (str[0] != '-' && ft_strlen(str) >= 10)
	{
		while (*str != 0 || "2147483647"[s - 1] != 0)
		{
			if ((*str - "2147483647"[s - 1]) > 0)
				return (1);
			s++;
			++str;
		}
	}
	return (0);
}